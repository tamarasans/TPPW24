// Operatori di post - incremento/decremento

let miaEta = 31;
console.log(miaEta);

console.log(miaEta++);
console.log(miaEta);
console.log(miaEta++);
console.log(miaEta--);


let tuaEta = 25;

console.log(tuaEta);
console.log(++tuaEta);
console.log(++tuaEta);
console.log(--tuaEta);


